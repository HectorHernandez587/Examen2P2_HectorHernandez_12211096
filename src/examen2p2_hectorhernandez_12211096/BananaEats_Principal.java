    public static void LlenarMatriz(JTextArea ta) {
        //Linea superior
        String s = "";
        for (int i = 0; i < 33; i++) {
            s += "*";
        }
        s += "\n";

        //centro de la forma
        for (int i = 0; i < 13; i++) {
            s += "*";
            for (int j = 0; j < 46; j++) {
                s += " ";
            }
            s += "*";
            s += "\n";
        }

        //Linea inferior
        for (int i = 0; i < 33; i++) {
            s += "*";
        }
        ta.setText(s);

    }
    

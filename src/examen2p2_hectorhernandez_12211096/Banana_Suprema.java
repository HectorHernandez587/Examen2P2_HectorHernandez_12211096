/**
 *
 * @author Hector
 */
public class Banana_Suprema {
    private int x;
    private int y;

    public Banana_Suprema() {
    }

    public Banana_Suprema(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Banana_Suprema{" + "x=" + x + ", y=" + y + '}';
    }
    
    
    
}
